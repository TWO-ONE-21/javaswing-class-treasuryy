    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int penarikann = Integer.parseInt(jTextField1.getText());
        String tujuann = jTextField2.getText();
        if (totalSaldo < penarikann){
            JOptionPane.showMessageDialog(rootPane, "Jumlah Pengeluaran Tidak Boleh Melebihi Total Saldo yang Tersedia!");
        } else if (tujuann.isEmpty() && penarikann == 0){
            JOptionPane.showMessageDialog(rootPane, "Masukkan Jumlah dan Tujuan!");
        } else if (tujuann.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Masukkan Tujuan!");
        } else if (penarikann == 0){
            JOptionPane.showMessageDialog(rootPane, "Masukkan Jumlah yang Benar!");
        } else {
            String keluar = jTextField1.getText(); 
            try {
                stat.executeUpdate("INSERT INTO pengeluaran (jumlah, tanggal, tujuan) VALUES ('" + penarikann + "', '" + tanggalNow + "', '" + tujuann + "')");
                JOptionPane.showMessageDialog(rootPane,"Rp. " + keluar + ",00 Telah Dicatat Sebagai Pengeluaran!");
                saldo();
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

