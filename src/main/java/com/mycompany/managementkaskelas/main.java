    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int ya = JOptionPane.showConfirmDialog(null, "Hapus " + jumlahBayar.getSelectedItem() + " Kali Pembayaran dari " + nama.getSelectedItem() + "?", "Konfirmasi Penghapusan!", JOptionPane.YES_NO_OPTION);
        
        if (ya == JOptionPane.YES_OPTION) {
            try {
                res = stat.executeQuery("SELECT * FROM mahasiswa WHERE nama='" + nama.getSelectedItem() + "'" );
                int sudahDibayar = 0;
                if (res.next()) {
                    sudahDibayar = res.getInt("total_pembayaran");
                }
                int jumlahBayarr = Integer.parseInt(jumlahBayar.getSelectedItem().toString());
                int pembayaran = sudahDibayar - jumlahBayarr;
                stat.executeUpdate("UPDATE mahasiswa SET total_pembayaran = " + pembayaran + " WHERE nama='" + nama.getSelectedItem() + "'");
                JOptionPane.showMessageDialog(rootPane, jumlahBayar.getSelectedItem() + " Kali Pembayaran dari " + nama.getSelectedItem() + " Berhasil Dihapus!");
                data();
                saldo();
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

